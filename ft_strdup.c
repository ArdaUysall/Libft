#include "libft.h"

char	*ft_strdup(const char *s1)
{
	size_t	len;
	size_t	i;
	char	*str;

	len = ft_strlen(s1);
	str = (char *)malloc((len + 1) * sizeof(char));
	i = 0;
	if (s1 == NULL)
		return (NULL);
	if (str == NULL)
		return (NULL);
	while (s1[i] != '\0')
	{
		str[i] = s1[i];
		i++;
	}
	str[i] = '\0';
	return (str);
}
